// ── Helpers ──────────────────────────────────────────────────────────────────
const TNR = "Times New Roman";
const MONO = "Courier New";
const FULL_W = 9360;

function p(text, opts = {}) {
  const size = (opts.size || 12) * 2;
  const runs = Array.isArray(text) ? text
    : [new TextRun({ text, font: opts.mono ? MONO : TNR, size: opts.mono ? 20 : size, bold: !!opts.bold, italics: !!opts.italics })];
  return new Paragraph({
    alignment: opts.align || AlignmentType.JUSTIFIED,
    spacing: { line: 360, after: opts.after !== undefined ? opts.after : 200 },
    indent: opts.indent ? { firstLine: 720 } : undefined,
    children: runs,
    pageBreakBefore: !!opts.pageBreak,
  });
}

function run(text, opts = {}) {
  return new TextRun({ text, font: opts.mono ? MONO : TNR, size: opts.mono ? 20 : (opts.size || 12) * 2, bold: !!opts.bold, italics: !!opts.italics, underline: opts.underline ? {} : undefined });
}

function emptyLine(count = 1) {
  const l = [];
  for (let i = 0; i < count; i++) l.push(new Paragraph({ spacing: { after: 200 }, children: [] }));
  return l;
}

function chapterHeading(num, title) {
  return [
    new Paragraph({ alignment: AlignmentType.CENTER, spacing: { after: 100 }, children: [run(`CHAPTER ${num}`, { size: 18, bold: true })], pageBreakBefore: true }),
    new Paragraph({ alignment: AlignmentType.CENTER, spacing: { after: 600 }, children: [run(title.toUpperCase(), { size: 18, bold: true })], heading: HeadingLevel.HEADING_1 }),
  ];
}

function sectionHeading(num, title) {
  return new Paragraph({ spacing: { before: 360, after: 200, line: 360 }, keepNext: true, children: [run(`${num}  ${title}`, { size: 14, bold: true })], heading: HeadingLevel.HEADING_2 });
}

function subHeading(num, title) {
  return new Paragraph({ spacing: { before: 240, after: 160, line: 360 }, keepNext: true, children: [run(`${num}  ${title}`, { size: 12, bold: true })], heading: HeadingLevel.HEADING_3 });
}

const border = { style: BorderStyle.SINGLE, size: 1, color: "000000" };
const borders = { top: border, bottom: border, left: border, right: border };

function tc(text, opts = {}) {
  return new TableCell({
    borders, width: { size: opts.width || 2340, type: WidthType.DXA },
    shading: opts.header ? { fill: "D9E2F3", type: ShadingType.CLEAR } : undefined,
    margins: { top: 60, bottom: 60, left: 100, right: 100 },
    children: [new Paragraph({ alignment: opts.align || AlignmentType.LEFT, children: [run(text, { size: 11, bold: !!opts.header })] })],
  });
}

function codeBlock(lines) {
  return lines.map(line => new Paragraph({
    spacing: { after: 0, line: 240 },
    indent: { left: 360 },
    children: [new TextRun({ text: line, font: MONO, size: 18 })],
  }));
}

function figImage(fileName, widthInches, heightInches, placeholderText) {
  const filePath = path.join(__dirname, fileName);
  if (fs.existsSync(filePath)) {
    const imgData = fs.readFileSync(filePath);
    return new Paragraph({
      alignment: AlignmentType.CENTER,
      spacing: { before: 200, after: 100 },
      children: [
        new ImageRun({ data: imgData, transformation: { width: Math.round(widthInches * 96), height: Math.round(heightInches * 96) }, type: "png" }),
      ],
    });
  }
  return p(placeholderText, { align: AlignmentType.CENTER, italics: true });
}

function figCaption(text) {
  return new Paragraph({ alignment: AlignmentType.CENTER, spacing: { before: 100, after: 300 }, children: [run(text, { size: 11, bold: true, italics: true })] });
}

function dotEntry(title, pg) {
  return new Paragraph({ spacing: { after: 120, line: 360 }, tabStops: [{ type: TabStopType.RIGHT, position: TabStopPosition.MAX }],
    children: [ run(title, { size: 12 }), new TextRun({ children: [new PositionalTab({ alignment: PositionalTabAlignment.RIGHT, relativeTo: PositionalTabRelativeTo.MARGIN, leader: PositionalTabLeader.DOT }), pg], font: TNR, size: 24 }) ] });
}

function makeTable(colWidths, headerRow, dataRows) {
  return new Table({
    width: { size: FULL_W, type: WidthType.DXA }, columnWidths: colWidths,
    rows: [
      new TableRow({ children: headerRow.map((h, i) => tc(h, { header: true, width: colWidths[i] })) }),
      ...dataRows.map(row => new TableRow({ children: row.map((c, i) => tc(c, { width: colWidths[i] })) })),
    ],
  });
}

